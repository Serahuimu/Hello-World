DATA SEGMENT
	X DB 3
	Y DB ?
DATA ENDS

CODE SEGMENT
	ASSUME CS:CODE, DS:DATA
START: 	
	MOV AX, DATA
	MOV DS, AX			;将AX中的数据送往数据段
	MOV AL, X			;X放AX的低八位
	CMP AL, 20
	JAE ABOVE
	MOV BL, 3
	MUL BL
	JMP RESULT			;跳转到RESULT段
ABOVE:
	SUB AL, 20
RESULT:
	MOV Y, AL
	MOV AH, 4CH
	INT 21H
CODE ENDS
	END START